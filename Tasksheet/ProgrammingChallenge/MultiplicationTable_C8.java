package ProgrammingChallenge;

public class MultiplicationTable_C8 {
    public static void main(String[] args) {
        int num = 10;

        for(int i = 0; i <= num; i++){
            for(int j = 0; j<=num; j++){
                System.out.print(i*j + "\t");     
            }
        System.out.println();
        }
        
    }

}
